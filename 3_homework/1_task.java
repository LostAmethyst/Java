public class Main {
    public static void main(String[] args) {
        // Заданный массив записей
        String[][] товары = {
                {"Товар 1 высший", "100", "1-й сорт"},
                {"Товар 2 высший", "200", "2-й сорт"},
                {"Товар 3", "150", "1-й сорт"},
                {"Товар 4", "250", "2-й сорт"},
                {"Товар 5 высший", "180", "1-й сорт"}
        };

        double максимальнаяЦена = 0;

        for (String[] товар : товары) {
            String название = товар[0];
            double цена = Double.parseDouble(товар[1]);
            String сорт = товар[2];

            if ((сорт.equals("1-й сорт") || сорт.equals("2-й сорт")) && название.contains("высший")) {
                if (цена > максимальнаяЦена) {
                    максимальнаяЦена = цена;
                }
            }
        }

        System.out.println("Наибольшая цена товаров 1-го или 2-го сорта с названием \"высший\": " + максимальнаяЦена);
    }
}
